// Load Category
function loadCategory(language, category) {
    const languageData = languagesData[language];
    const categoryData = languageData.categories[category];

    if (!categoryData) return;

    // Update the "Basic Words and Phrases" section with selected category's words
    const vocabularyList = document.getElementById("vocabulary-list");
    vocabularyList.innerHTML = categoryData
        .map(item => `<li>${item.word} - ${item.translation}</li>`)
        .join("");

    // Update the section title dynamically
    const phrasesDisplay = document.getElementById("phrases-display");
    phrasesDisplay.textContent = `Learn about ${category}`;
}



       


