const languageSelect = document.getElementById("language-select");
const funFactText = document.getElementById("fun-fact");
const categorySelect = document.getElementById("category-select");
const wordList = document.getElementById("word-list");

languageSelect.addEventListener("change", updateCategories);
categorySelect.addEventListener("change", updateWordList);

function updateCategories() {
    const language = languageSelect.value;
    const categories = Object.keys(languagesData[language]?.categories || {});
    categorySelect.innerHTML = categories.map(cat => `<option value="${cat}">${cat}</option>`).join("");
    updateFunFact(language);
    updateWordList();
}

function updateFunFact(language) {
    funFactText.textContent = languagesData[language]?.funFact || "Select a language to see a fun fact!";
}

function updateWordList() {
    const language = languageSelect.value;
    const category = categorySelect.value;
    const words = languagesData[language]?.categories[category] || [];

    wordList.innerHTML = words.map(item => `<li>${item.word} - ${item.translation}</li>`).join("");
}

updateCategories();

       


